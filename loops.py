#While loop
#1. Initializing the first value
value = 5

#2. Setting the condition
while value <= 10 :
    print(value)
    value += 1 #appreciation

#part 2 (depreciation)
value2 = 30
while 30 >= value2 > 0:
    print(value2)
    value2 -= 1

#FOR loop
for x in range(105,110) :
    print("Number is", x)

devices = ["laptop","mouse","computer"]
print(devices)

for y in devices :
    print(y)
